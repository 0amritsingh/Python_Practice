# Problem: Write a Python program to test whether a passed letter is a vowel or not.

is_vowel = input("Check you letter is a vowel or not: ")
if is_vowel == "a":
    print(f"{is_vowel} is a Vowel")
elif is_vowel == "e":
    print(f"{is_vowel} is a Vowel")
elif is_vowel == "i":
    print(f"{is_vowel} is a Vowel")
elif is_vowel == "o":
    print(f"{is_vowel} is a Vowel")
elif is_vowel == "u":
    print(f"{is_vowel} is a Vowel")
else:
    print(f"{is_vowel} is NOT a Vowel")
